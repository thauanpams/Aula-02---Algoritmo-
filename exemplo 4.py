#Implemente um algoritmo que receba 4 notas de um aluno e retire a média

nota1 = float(input(f'Primeira nota: '))
nota2 = float(input(f'Segunda nota: '))
nota3 = float(input(f'Terceira nota: '))
nota4 = float(input(f'Quarta nota: '))

media_das_notas = (nota1 + nota2 + nota3 + nota4)/4 

print(f'Média das notas: {media_das_notas} ')





